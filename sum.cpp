#include<iostream>
using namespace std;

int main()
{
    int n1,n2;
    cout<<"Enter first number:\n";// '<<' is insertion operator
    cin>>n1;// '>>' is extraction operator
    cout<<"Enter second number:\n";
    cin>>n2;
    cout<<"Sum of two number is:"<<n1+n2;
    
    return 0;
}